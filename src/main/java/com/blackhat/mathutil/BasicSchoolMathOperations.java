package com.blackhat.mathutil;

public class BasicSchoolMathOperations {

	public int add(int numberOne, int numberTwo) {
		return numberOne + numberTwo;
	}

	public int subtract(int numberOne, int numberTwo) {
		return numberOne - numberTwo;
	}

	public int divide(int numberOne, int numberTwo) {
		return numberOne / numberTwo;
	}

	public int multiplication(int numberOne, int numberTwo) {
		return numberOne * numberTwo;
	}

	public float circleCompute(float radius) {
		return radius*radius;
	}

}
